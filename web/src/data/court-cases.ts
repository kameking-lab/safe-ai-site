/** ID から判例を取得。 */
export function getCourtCaseById(id: string): CourtCase | undefined {
  return COURT_CASES.find((c) => c.id === id);
}

/** 争点ごとの件数。 */
export function countByIssue(): Record<CourtCaseIssue, number> {
  const out = Object.fromEntries(COURT_CASE_ISSUES.map((i) => [i, 0])) as Record<CourtCaseIssue, number>;
  for (const c of COURT_CASES) for (const i of c.issues) out[i] += 1;
  return out;
}

