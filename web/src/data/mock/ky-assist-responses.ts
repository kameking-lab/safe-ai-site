export type KyAssistField = "hazard" | "reduction" | "rereduction";

type KyAssistInput = {
  field: KyAssistField;
  targetLabel: string;
  workContext: string;
  hazardSoFar?: string;
  reductionSoFar?: string;
  likelihood?: number;
  severity?: number;
  reLikelihood?: number;
  reSeverity?: number;
  seed?: number;
};

function hashSeed(s: string, n: number) {
  let h = n;
  for (let i = 0; i < s.length; i += 1) h = (h * 31 + s.charCodeAt(i)) >>> 0;
  return h;
}

