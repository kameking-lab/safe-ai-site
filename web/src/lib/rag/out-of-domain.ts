/**
 * クエリがドメイン外シグナルを持つ（＝減点対象）かどうか。
 * ドメイン外語を含み、かつ労働・現場文脈の語を1つも含まない場合のみ true。
 */
export function hasOutOfDomainSignal(query: string): boolean {
  if (!OUT_OF_DOMAIN_TERMS.some((t) => query.includes(t))) return false;
  return !IN_DOMAIN_GUARDS.some((t) => query.includes(t));
}

/**
 * ドメイン外シグナルつきクエリへの信頼度減点係数。
 * 偶発共起によるスコア（実測0.6台）を CONFIDENCE_THRESHOLD(0.5) 未満へ
 * 落とすため 0.5 を掛ける（0.62→0.31 / 0.66→0.33）。
 */
export const OUT_OF_DOMAIN_PENALTY_FACTOR = 0.5;
