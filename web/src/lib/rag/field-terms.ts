/**
 * 横断検索用: 1語（AND クエリの1ターム）に対する現場語彙の展開語を返す。
 * TERM_EXPANSIONS（RAG共有）と SEARCH_ONLY_TERM_EXPANSIONS（検索専用）の合算。
 * キーは部分一致（term.includes(key)）＝「ユンボの」「玉掛けの」でも発火する。
 */
export function expandFieldTermsForSearch(term: string): string[] {
  const additions: string[] = [];
  for (const dict of [TERM_EXPANSIONS, SEARCH_ONLY_TERM_EXPANSIONS]) {
    for (const [key, vals] of Object.entries(dict)) {
      if (term.includes(key)) additions.push(...vals);
    }
  }
  return [...new Set(additions)];
}
